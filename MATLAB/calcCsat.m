function [Csat] = calcCsat(T, P)
load('constants')
% Calculate oxygen saturation concentration (mol/m^3)
Tref = 273.15;
Csat =(exp(7.7117-1.31403.*log(T+45.93)).*P ...
    .*(1-exp(11.8571-(3840.7./(T+Tref))-(216961./((T+Tref).^2)))./P) ...
    .*(1-(0.000975-(0.00001426.*T)+(0.00000006436.*(T.^2))).*P)) ...
    ./(1-exp(11.8571-(3840.7./(T+Tref))-(216961./((T+Tref).^2)))) ...
    ./(1-(0.000975-(0.00001426.*T)+(0.00000006436.*(T.^2))))./const.MMO2;